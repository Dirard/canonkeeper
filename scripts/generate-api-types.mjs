import { execFile } from 'node:child_process';
import { mkdtemp, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import path from 'node:path';
import process from 'node:process';
import { promisify } from 'node:util';

const execFileAsync = promisify(execFile);

const contractPath = path.resolve('contracts/openapi.json');
const openapiOutputPath = path.resolve('src/shared/api/generated/openapi.ts');
const manifestOutputPath = path.resolve('src/shared/api/generated/operation-manifest.ts');
const checkMode = process.argv.includes('--check');
const httpMethods = ['get', 'post', 'put', 'patch', 'delete'];

function slashPath(filePath) {
  return path.relative(process.cwd(), filePath).split(path.sep).join('/');
}

function literal(value) {
  return JSON.stringify(value);
}

function pathParams(apiPath) {
  return [...apiPath.matchAll(/\{([^}]+)\}/g)].map((match) => match[1]);
}

async function generateOpenApiTypes(outputPath) {
  const cliPath = path.resolve('node_modules/openapi-typescript/bin/cli.js');
  await execFileAsync(process.execPath, [cliPath, contractPath, '--output', outputPath], {
    cwd: process.cwd(),
    windowsHide: true,
  });
}

function generateOperationManifest(spec) {
  const entries = [];
  const seen = new Set();

  for (const [apiPath, pathItem] of Object.entries(spec.paths ?? {})) {
    for (const method of httpMethods) {
      const operation = pathItem[method];
      if (!operation?.operationId) {
        continue;
      }

      if (seen.has(operation.operationId)) {
        throw new Error(`Duplicate operationId in OpenAPI contract: ${operation.operationId}`);
      }
      seen.add(operation.operationId);

      const params = pathParams(apiPath).map(literal).join(', ');
      entries.push(
        `  ${literal(operation.operationId)}: { method: ${literal(method.toUpperCase())}, path: ${literal(apiPath)}, pathParams: [${params}] },`,
      );
    }
  }

  entries.sort((a, b) => a.localeCompare(b));

  return `// This file is generated by scripts/generate-api-types.mjs.
// Source: contracts/openapi.json

import type { operations, paths } from './openapi';

export const operationManifest = {
${entries.join('\n')}
} as const satisfies Record<
  keyof operations,
  { method: 'GET' | 'POST' | 'PUT' | 'PATCH' | 'DELETE'; path: keyof paths; pathParams: readonly string[] }
>;

export type OperationId = keyof typeof operationManifest;
export type OperationRoute<TOperation extends OperationId> = (typeof operationManifest)[TOperation];
`;
}

async function generateAll(openapiPath, manifestPath) {
  const raw = await readFile(contractPath, 'utf8');
  const spec = JSON.parse(raw);
  await mkdir(path.dirname(openapiPath), { recursive: true });
  await generateOpenApiTypes(openapiPath);
  await writeFile(manifestPath, generateOperationManifest(spec), 'utf8');
}

if (checkMode) {
  const tmpRoot = await mkdtemp(path.join(tmpdir(), 'canonkeeper-openapi-'));
  const tmpOpenApiPath = path.join(tmpRoot, 'openapi.ts');
  const tmpManifestPath = path.join(tmpRoot, 'operation-manifest.ts');

  try {
    await generateAll(tmpOpenApiPath, tmpManifestPath);
    const [currentOpenApi, nextOpenApi, currentManifest, nextManifest] = await Promise.all([
      readFile(openapiOutputPath, 'utf8').catch(() => ''),
      readFile(tmpOpenApiPath, 'utf8'),
      readFile(manifestOutputPath, 'utf8').catch(() => ''),
      readFile(tmpManifestPath, 'utf8'),
    ]);

    const stale = [];
    if (currentOpenApi !== nextOpenApi) {
      stale.push(slashPath(openapiOutputPath));
    }
    if (currentManifest !== nextManifest) {
      stale.push(slashPath(manifestOutputPath));
    }

    if (stale.length > 0) {
      console.error(`Generated API boundary is stale: ${stale.join(', ')}`);
      process.exit(1);
    }

    console.log(
      `Generated API boundary is current: ${slashPath(openapiOutputPath)}, ${slashPath(manifestOutputPath)}`,
    );
  } finally {
    await rm(tmpRoot, { force: true, recursive: true });
  }
} else {
  await generateAll(openapiOutputPath, manifestOutputPath);
  console.log(`Generated API boundary written: ${slashPath(openapiOutputPath)}, ${slashPath(manifestOutputPath)}`);
}
